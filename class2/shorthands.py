# We can assign multiple variables with multiple values with 1 LOC(Line Of Code) also.
# x = 1
# y = 2
# z = 3

x, y, z = 1, 2, 3
print(x, y, z)

# We can also assign same value to diff variables with single line only

x = y = z = 1
print(x, y, z)

# We can assign list of data to certain variables
names = ["Nitin", "Yash", "Ujjwal", "Anji"]
# a,b,c,d
a, b, c, d = names
print(a, b, c, d)

